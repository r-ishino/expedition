// TODO [post-PoC]: タスクステータス遷移ロジック
//
// タスクのステータス遷移を管理するステートマシン。
//
// ステータス一覧:
//   未着手 → 細分化中 → 実装待ち → 実装中 → PR作成済 → CI実行中 → レビュー待ち → マージ済 → 完了
//                                     ↑                       │
//                                     └── CI失敗・修正中 ←────┘
//
// 提供する関数:
// - canTransition(from, to): 遷移可能かどうかを判定
// - transition(task, to): ステータスを遷移させる（不正な遷移はエラー）
// - getNextStatuses(current): 現在のステータスから遷移可能なステータス一覧
//
// 各ステータスは自動遷移を基本とし、手動遷移もUIから可能にする。
// Jiraへは主要なステータス（着手/レビュー中/完了）のみ同期する。
